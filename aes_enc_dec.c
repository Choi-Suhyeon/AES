void swap(pbyte x, pbyte y) {
    *x ^= *y; *y ^= *x; *x ^= *y;
}

void toggleState(pbyte block) {
    swap(block + 0x1, block + 0x4);
    swap(block + 0x2, block + 0x8);
    swap(block + 0x3, block + 0xC);
    swap(block + 0x6, block + 0x9);
    swap(block + 0x7, block + 0xD);
    swap(block + 0xB, block + 0xE);
}

