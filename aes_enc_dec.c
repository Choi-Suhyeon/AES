#include "aes_enc_dec.h"

#define MUL2(x) (((x) << 1) ^ ((x) & 0x80 ? 0x1B : 0))
#define MUL3(x) (MUL2((x)) ^ (x))
#define MUL4(x) (MUL2(MUL2((x))))
#define MUL8(x) (MUL2(MUL4((x))))
#define MUL9(x) (MUL8((x)) ^ (x))
#define MULB(x) (MUL8((x)) ^ MUL2((x)) ^ (x))
#define MULD(x) (MUL8((x)) ^ MUL4((x)) ^ (x))
#define MULE(x) (MUL3(MUL2((x))) ^ MUL8((x)))

#define ADD_RND_KEY(s, k)       \
__asm__ __volatile__ (          \
    "movq (%%rsi), %%rbx  \n\t" \
    "xorq %%rbx, (%%rdi)  \n\t" \
    "movq 8(%%rsi), %%rbx \n\t" \
    "xorq %%rbx, 8(%%rdi) \n\t" \
    :: "D"((s)), "S"((k))       \
    : "rbx"                     \
)

#define ROT_WORD(s, n)         \
__asm__ __volatile__ (         \
    "xorq %%rbx, %%rbx   \n\t" \
    "movl (%%rdi), %%ebx \n\t" \
    "rorl $"#n", %%ebx   \n\t" \
    "movl %%ebx, (%%rdi) \n\t" \
    :: "D"((s))                \
    : "rbx"                    \
)

#define INV_ROT_WORD(s, n)     \
__asm__ __volatile__ (         \
    "xorq %%rbx, %%rbx   \n\t" \
    "movl (%%rdi), %%ebx \n\t" \
    "roll $"#n", %%ebx   \n\t" \
    "movl %%ebx, (%%rdi) \n\t" \
    :: "D"((s))                \
    : "rbx"                    \
)

#define MIX_COL_0X(w, x, y, z) (MUL2((w)) ^ MUL3((x)) ^ (y) ^ (z))                        
#define MIX_COL_1X(w, x, y, z) (MUL2((x)) ^ MUL3((y)) ^ (z) ^ (w))
#define MIX_COL_2X(w, x, y, z) (MUL2((y)) ^ MUL3((z)) ^ (w) ^ (x))
#define MIX_COL_3X(w, x, y, z) (MUL2((z)) ^ MUL3((w)) ^ (x) ^ (y))

#define INV_MIX_COL_0X(w, x, y, z) (MULE((w)) ^ MULB((x)) ^ MULD((y)) ^ MUL9((z)))
#define INV_MIX_COL_1X(w, x, y, z) (MULE((x)) ^ MULB((y)) ^ MULD((z)) ^ MUL9((w)))
#define INV_MIX_COL_2X(w, x, y, z) (MULE((y)) ^ MULB((z)) ^ MULD((w)) ^ MUL9((x)))
#define INV_MIX_COL_3X(w, x, y, z) (MULE((z)) ^ MULB((w)) ^ MULD((x)) ^ MUL9((y)))

